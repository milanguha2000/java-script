// while loop

let i = 0
while (i <= 10) {
    console.log(`value of i is ${i}`);
    i = i + 2
}


let myArray = ["batman" , "ironman" , "superman" , "spiderman"]

let arr = 0
while (arr < myArray.length) {
    console.log(`value is ${myArray[arr]}`);
    arr = arr + 1
}


